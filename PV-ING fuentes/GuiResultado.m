% --- Executes just before GuiResultado is made visible.
function GuiResultado_OpeningFcn(hObject, eventdata, handles, varargin)
% This function has no output args, see OutputFcn.
% hObject    handle to figure
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
% varargin   command line arguments to GuiResultado (see VARARGIN)

handles.MFilePath=fileparts(which(mfilename));
guidata(hObject, handles);

if length(varargin)>0
    handles.resultado=varargin{1,1};
    handles.IsResultado=varargin{1,2};
    if handles.IsResultado
        %%  llevo struct a matriz comun 
        %a=cell2mat(handles.resultado);
        %b=struct2cell(a);
        %c=cell2mat(b);
        %c=c'; % aca tengo el resultado en version matriz
        %%  obtengo los nombres
        names=fields(handles.resultado);
        try
            for i=1:length(names)
                if i==1
                    data=handles.resultado.(names{i});
                else
                    data2=handles.resultado.(names{i});
                    data=horzcat(data,data2);
                end
            end
            set(handles.uitable4,'Data',data);
            set(handles.uitable4,'ColumnName',names);
            set(handles.edit24, 'String', 'ResultadoSalida');
            set(handles.edit25, 'String', 'ResultadoSalida');             
            set(handles.listbox1,'String',names);
            set(handles.listbox2,'String',names);       
            set(handles.listbox7,'String',names);
            
            handles.IsResultado=true;            
        catch ME
            warndlg('Problema con resultado','No se importo resultado correctamente');
            disp(ME.identifier);
            disp(ME.message);
            handles.IsResultado=false;
            set(handles.edit24, 'String', 'Clear');
            set(handles.edit25, 'String', 'Clear');  
        end 
              
    else
        set(handles.listbox1,'String','Clear');
        set(handles.listbox2,'String','Clear'); 
        set(handles.listbox7,'String','Clear');
        set(handles.edit24, 'String', 'Clear');
        set(handles.edit25, 'String', 'Clear');          
        clear handles.resultado;
        handles.resultado=struct();        
        handles.IsResultado=false;             
    end     
else
   %% armo estructura resultado
    set(handles.listbox1,'String','Clear');
    set(handles.listbox2,'String','Clear'); 
    set(handles.listbox7,'String','Clear');
    set(handles.edit24, 'String', 'Clear');
    set(handles.edit25, 'String', 'Clear');    
    handles.IsResultado=false; 
    handles.resultado=struct();
    
end

set(handles.listbox5,'String','Clear');
set(handles.listbox6,'String','Clear');  
set(handles.listbox8,'String','Clear');
set(handles.edit23, 'String', 'Clear');
set(handles.edit26, 'String', 'Clear');   
% Choose default command line output for GuiResultado
handles.output = hObject;
handles.IsComparacion=false;
% Update handles structure
guidata(hObject, handles);
%uiwait(hObject);
% UIWAIT makes GuiResultado wait for user response (see UIRESUME)
% uiwait(handles.figure2);


