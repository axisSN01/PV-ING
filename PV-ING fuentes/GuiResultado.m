    if Name == 0
        return;
    else  
        try
            fullPath=strcat(path,Name);
            [data names]= xlsread(fullPath);% leo directamente una matriz
            %%  obtengo los nombres y cargo en uitable
            set(handles.uitable4,'Data',data);
            set(handles.uitable4,'ColumnName',names);
            for i=1:length(names)
                handles.resultado.(names{i})=data(:,i); 
            end                     
