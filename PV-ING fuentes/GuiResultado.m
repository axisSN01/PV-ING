            clear handles.resultado;
            set(handles.listbox1,'String','Clear');       
            set(handles.listbox2,'String','Clear');
            set(handles.listbox7,'String','Clear');            
            set(handles.edit24, 'String','');
            set(handles.edit25, 'String', '');             
        end

    end
    guidata(hObject, handles);  
